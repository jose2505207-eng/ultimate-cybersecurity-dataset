"""Render the gold unified ``dataset_card.md`` from a manifest dict."""

from __future__ import annotations

from typing import Any

from cyberdataset.gold.schema import DOMAIN_LABELS, GOLD_UNIFIED_COLUMNS


def _counts_table(title: str, counts: dict[str, Any], *, label_map: dict[str, str] | None = None) -> str:
    if not counts:
        return f"### {title}\n\n_None._\n"
    lines = [f"### {title}\n", "| Key | Records |", "| --- | ---: |"]
    for key, value in sorted(counts.items(), key=lambda kv: (-kv[1], kv[0])):
        display = label_map.get(key, key) if label_map else key
        lines.append(f"| {display} | {value:,} |")
    return "\n".join(lines) + "\n"


def render_dataset_card(manifest: dict[str, Any]) -> str:
    """Produce a recruiter- and researcher-friendly dataset card in Markdown."""
    total = manifest.get("total_records", 0)
    schema_fields = "`, `".join(GOLD_UNIFIED_COLUMNS)

    sections: list[str] = []
    sections.append("# Gold Unified Cybersecurity Dataset — Dataset Card\n")
    sections.append(
        "_Auto-generated by `cyberdataset.gold.build_gold`. Do not edit by hand; "
        "rebuild to refresh._\n"
    )

    sections.append("## What this is\n")
    sections.append(
        "The **gold unified layer** is a single, flat, AI-training-ready dataset that "
        "merges every per-source *silver* dataset in this repository into one canonical "
        "schema spanning all covered cybersecurity domains. Each row is a deduplicated, "
        "quality-scored, split-assigned example with full source attribution.\n"
    )

    sections.append("## Canonical schema\n")
    sections.append(
        f"Every record carries exactly these fields:\n\n`{schema_fields}`\n\n"
        "`mitre_attack_ids` is a list; `entities` and `metadata` are JSON objects "
        "(kept native in JSONL, JSON-encoded in Parquet/CSV).\n"
    )

    sections.append("## Bronze -> Silver -> Gold\n")
    sections.append(
        "- **Bronze** (`data/bronze_raw/`): untouched raw cybersecurity sources, "
        "collectively **~200GB+**. Bronze is immutable input and is never cleaned in place.\n"
        "- **Silver** (`data/silver_normalized/`): per-source datasets cleaned and "
        "normalized into a shared, source-specific schema with safe representations.\n"
        "- **Gold** (this layer): all silver rows normalized into one unified schema, "
        "deduplicated by stable content hash, quality-scored, and split deterministically "
        "into train/val/test.\n"
    )

    sections.append("## Domains covered\n")
    domain_lines = ["| Domain | Description |", "| --- | --- |"]
    for slug, label in DOMAIN_LABELS.items():
        domain_lines.append(f"| `{slug}` | {label} |")
    sections.append("\n".join(domain_lines) + "\n")

    sections.append("## Composition\n")
    sections.append(f"- **Total records:** {total:,}\n")
    sections.append(f"- **Mean quality score:** {manifest.get('mean_quality_score', 0.0)}\n")
    sections.append(f"- **Duplicates removed:** {manifest.get('duplicates_removed', 0):,}\n")
    sections.append(f"- **Min quality threshold:** {manifest.get('min_quality')}\n")
    sections.append(f"- **Split seed:** {manifest.get('seed')}\n")

    sections.append(_counts_table("Records by domain", manifest.get("counts_by_domain", {}),
                                  label_map=DOMAIN_LABELS))
    sections.append(_counts_table("Records by split", manifest.get("counts_by_split", {})))
    sections.append(_counts_table("Records by label", manifest.get("counts_by_label", {})))
    sections.append(_counts_table("Records by source", manifest.get("counts_by_source", {})))

    sections.append("## Fresh data\n")
    sections.append(
        "New public-source data is pulled by the fresh-data scraper "
        "(`python -m cyberdataset.scrapers.fetch_fresh`), which writes raw records under "
        "`data/bronze_raw/fresh/<source_id>/<date>/`. Fresh bronze is normalized by the "
        "matching `cyberdataset.ingest.*` adapter into silver, then merged here by "
        "re-running the gold build. No paid or proxy service is required.\n"
    )

    sections.append("## Intended use\n")
    sections.append(
        "- Benchmarking and regression-testing cybersecurity classifiers, detectors, and agents.\n"
        "- Training and evaluating defensive cybersecurity AI models.\n"
        "- Analytics and research over normalized, multi-domain security data.\n"
    )

    sections.append("## Limitations & ethical use\n")
    sections.append(
        "- Labels and domain inference are heuristic and source-dependent; verify before "
        "high-stakes use.\n"
        "- Source licenses vary per row (`source_license`); confirm upstream terms before "
        "redistribution.\n"
        "- Content is stored as **safe representations** for defensive research only.\n"
        "- **NOT** for offensive misuse: do not use this data to generate live exploits, "
        "malware execution workflows, credentials, or operational attack payloads.\n"
    )

    return "\n".join(sections) + "\n"
